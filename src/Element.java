
public class Element
{
	private Oznaka oznaka;
	private int broj;
	
	public Element(Oznaka oznaka)
	{
		this.oznaka = oznaka;
	}

	public Oznaka getOznaka()
	{
		return oznaka;
	}

	public void setOznaka(Oznaka oznaka)
	{
		this.oznaka = oznaka;
	}

	public int getBroj()
	{
		return broj;
	}

	public void setBroj(int broj)
	{
		this.broj = broj;
	}
	
	
}
